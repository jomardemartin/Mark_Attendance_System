    private void jButton1_loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1_loginActionPerformed
        // TODO add your handling code here:
        String teacherfname = teacher_fname.getText();
        String teacherlname = teacher_lname.getText();
        String teacheraddress = teacher_address.getText();
        String teachergender = teacher_gender.getSelectedItem().toString();
        String teacherdept = department.getSelectedItem().toString();
        String teachersubject = subject.getSelectedItem().toString();
        String teacherusername = teacher_username.getText();
        String teacheremail = teacher_email.getText();
        String teacherpassword = String.valueOf(teacher_password.getPassword());
        String teachercpassword = String.valueOf(teacher_cpassword.getPassword());
        String teachersecuq = teacher_securityq.getSelectedItem().toString();
        String teachersecuans = teacher_securityans.getText();
        String usertype = "Teacher";
        
        //Field requirements for registration
        if (teacherfname.equals("")){
            JOptionPane.showMessageDialog(null, "Input First Name!");
        }
        else if (teacheraddress.equals("")){
            JOptionPane.showMessageDialog(null, "Input Last Name!");
        }
        else if (teacheraddress.equals("")){
            JOptionPane.showMessageDialog(null, "Input Address!");
        }
        else if (teachergender.equals("")){
            JOptionPane.showMessageDialog(null, "Select Gender!");
        }
        else if (teacherdept.equals("")){
            JOptionPane.showMessageDialog(null, "Select Department!");
        }
        else if (teachersubject.equals("")){
            JOptionPane.showMessageDialog(null, "Select Subject!");
        }
        else if (teacherusername.equals("")){
            JOptionPane.showMessageDialog(null, "Username is required!");
        }
        else if (teacheremail.equals("")){
            JOptionPane.showMessageDialog(null, "Input Email!");
        }
        else if (!teacherpassword.equals(teachercpassword)){
            JOptionPane.showMessageDialog(null, "Password doesn't match!");
        }
        else if (teachersecuans.equals("")){
            JOptionPane.showMessageDialog(null, "Input Security Answer!");
        }
        else{
            PreparedStatement ps;
            PreparedStatement ps2;
            String query = "INSERT INTO teacher_info(teacher_fname, teacher_lname, teacher_address, teacher_gender, subject, department)VALUES (?, ?, ?, ?, ?, ?)";
            String query2 = "INSERT INTO user_info(fname, lname, username, email, password, cpassword, security_q, security_a, user_type)VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?)";
            int status = 0;
            try{
                
                
                ps2 = MySQL_Connection.getConnection().prepareStatement(query2);
                ps2.setString(1, teacherfname);
                ps2.setString(2, teacherlname);
                ps2.setString(3, teacherusername);
                ps2.setString(4, teacheremail);
                ps2.setString(5, teacherpassword);
                ps2.setString(6, teachercpassword);
                ps2.setString(7, teachersecuq);
                ps2.setString(8, teachersecuans);
                ps2.setString(9, usertype);
               
                status = ps2.executeUpdate();
                
            }

            catch(SQLException ex){
                Logger.getLogger(LoginTeacherForm.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(null, "Username already exists! Use other usernames instead");
            }
            
            if(status > 0){
                
                try{
                ps = MySQL_Connection.getConnection().prepareStatement(query);
                ps.setString(1, teacherfname);
                ps.setString(2, teacherlname);
                ps.setString(3, teacheraddress);
                ps.setString(4, teachergender);
                ps.setString(5, teachersubject);
                ps.setString(6, teacherdept);
                ps.executeUpdate();
                JOptionPane.showMessageDialog(null, "Registration Successful");
                } catch(SQLException ex){
                        Logger.getLogger(LoginStudentForm.class.getName()).log(Level.SEVERE, null, ex);
                 }
                
            }
        }
    }//GEN-LAST:event_jButton1_loginActionPerformed

